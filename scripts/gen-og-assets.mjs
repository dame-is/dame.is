#!/usr/bin/env node
// Generates the embedded assets that the OG-image + favicon serverless
// functions depend on, and writes them as committed base64 modules under
// `og/assets/`. Run manually whenever the fonts or the sky-avatar art change:
//
//   node scripts/gen-og-assets.mjs
//
// Why base64-in-a-module instead of reading files at runtime? Static assets
// under `images/` are not reliably served in production (the SPA catch-all
// rewrite swallows them), and Vercel's function file-tracing can miss files
// read dynamically. Importing a module guarantees the bytes ship with the
// function. The avatars are small (favicon-sized), so the footprint is modest.

import { writeFile, mkdir, readFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import sharp from 'sharp';
import { avatarKeys } from '../og/time.js';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const OUT = resolve(ROOT, 'og/assets');
const SKY = resolve(ROOT, 'images/sky-avatars');

// Two families, both fetched as static woff from the Fontsource CDN (satori
// accepts woff):
//   • Crimson Pro — the site's serif (src/styles/theme.css --font-crimson),
//     for the breadcrumb, big title, and description.
//   • IBM Plex Mono — the site's mono (--mono), for the notebook folio
//     (day-of-life) and the AT-Protocol NSID marginalia.
// The `id` keys are what api/og.js maps back to {name, weight, style}.
const CRIMSON = 'https://cdn.jsdelivr.net/npm/@fontsource/crimson-pro@5/files';
const PLEX = 'https://cdn.jsdelivr.net/npm/@fontsource/ibm-plex-mono@5/files';
const FONT_SPECS = [
  { id: '300', url: `${CRIMSON}/crimson-pro-latin-300-normal.woff` },
  { id: '400', url: `${CRIMSON}/crimson-pro-latin-400-normal.woff` },
  { id: '600', url: `${CRIMSON}/crimson-pro-latin-600-normal.woff` },
  { id: '700', url: `${CRIMSON}/crimson-pro-latin-700-normal.woff` },
  { id: '400i', url: `${CRIMSON}/crimson-pro-latin-400-italic.woff` },
  { id: '600i', url: `${CRIMSON}/crimson-pro-latin-600-italic.woff` },
  { id: 'mono400', url: `${PLEX}/ibm-plex-mono-latin-400-normal.woff` },
  { id: 'mono500', url: `${PLEX}/ibm-plex-mono-latin-500-normal.woff` },
];

// Favicon (+ apple-touch) + the small brand mark in the OG chrome bar all
// draw from one 96px PNG set — crisp on retina tabs, universal browser
// support. An avatar-forward OG variant would want a larger JPEG set; add it
// back here if that layout is ever chosen.
const ICON_SIZE = 96;

async function fonts() {
  const out = {};
  for (const { id, url } of FONT_SPECS) {
    const res = await fetch(url);
    if (!res.ok) throw new Error(`font fetch ${url}: ${res.status}`);
    out[id] = Buffer.from(await res.arrayBuffer()).toString('base64');
  }
  return out;
}

async function avatars() {
  const icons = {};
  for (const key of avatarKeys()) {
    const src = resolve(SKY, `${key}.jpg`);
    const raw = await readFile(src);
    icons[key] = (await sharp(raw).resize(ICON_SIZE, ICON_SIZE, { fit: 'cover' }).png().toBuffer()).toString('base64');
  }
  return { icons };
}

function moduleSource(name, obj, note) {
  const entries = Object.entries(obj)
    .map(([k, v]) => `  ${JSON.stringify(k)}: ${JSON.stringify(v)},`)
    .join('\n');
  return `// GENERATED by scripts/gen-og-assets.mjs — do not edit by hand.\n// ${note}\nexport const ${name} = {\n${entries}\n};\n`;
}

async function main() {
  await mkdir(OUT, { recursive: true });

  const FONTS = await fonts();
  await writeFile(resolve(OUT, 'fonts.js'), moduleSource('FONTS', FONTS, 'Crimson Pro woff fonts, base64.'));

  const { icons } = await avatars();
  await writeFile(resolve(OUT, 'icons.js'), moduleSource('ICONS', icons, `${ICON_SIZE}px sky-avatar PNGs, base64 (favicon + apple-touch + OG mark).`));

  const sum = (o) => Object.values(o).reduce((n, v) => n + Buffer.byteLength(v, 'base64'), 0);
  console.log('fonts.js  ', (sum(FONTS) / 1024).toFixed(0), 'KB decoded');
  console.log('icons.js  ', (sum(icons) / 1024).toFixed(0), 'KB decoded');
  console.log('done →', OUT);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
